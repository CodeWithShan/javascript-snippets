// Concatination in JavaScript

const devRoles = ["Front-End Developer", "Back-End Developer", "Full-Stack Developer"];

const desRoles = ["Illustration Designer", "Photoshop Artist", "Creative Director",];

const roles = devRoles.concat(desRoles);


console.log(roles, "Hello".concat(" Digital", " World"));


